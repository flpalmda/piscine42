int	ft_str_is_numeric(char *str)
{
	int c;
	int n;

	c = 0;
	n = 0;
	while (str[c] != '\0')
	{
		if (str[c] > 47 && str[c] < 58)
			n++;
		c++;
	}
	if (c == n)
		return (1);
	else
		return (0);
}
