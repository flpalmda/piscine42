int	ft_str_is_lowercase(char *str)
{
	int c;
	int l;

	c = 0;
	l = 0;
	while (str[c] != '\0')
	{
		if (str[c] > 96 && str[c] < 123)
			l++;
		c++;
	}
	if (c == l)
		return (1);
	else
		return (0);
}
