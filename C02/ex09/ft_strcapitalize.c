char	*ft_strcapitalize(char *str)
{
	int c;

	c = 0;
	while (str[c] != '\0')
	{
		if (c == 0 && str[c] >= 'a' && str[c] <= 'z')
			str[c] -= 32;
		else if (str[c] > 64 && str[c] < 91)
			str[c] += 32;
		if ((str[c - 1] < 48 || str[c - 1] > 122) ||
				(str[c - 1] > 57 && str[c - 1] < 65) ||
				(str[c - 1] > 90 && str[c - 1] < 97))
		{
			if (str[c] > 96 && str[c] < 123)
				str[c] -= 32;
		}
		c++;
	}
	return (str);
}
