int	ft_is_prime(int nb)
{
	int c;

	if (nb <= 1)
		return (0);
	if (nb == 2 || nb == 3)
		return (1);
	if (nb % 2 == 0)
		return (0);
	c = 3;
	while (c < (nb / 2))
	{
		if (nb % c == 0)
			return (0);
		c += 2;
	}
	return (1);
}
